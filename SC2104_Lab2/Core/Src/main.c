/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
	/* USER CODE BEGIN 1 */

	ICM20948 imu;
	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_USART3_UART_Init();
	MX_I2C1_Init();
	/* USER CODE BEGIN 2 */
	OLED_Init();
	OLED_ShowString(0, 5, "Oh wow SC2104 sure is fun :3");
	OLED_Refresh_Gram();

	HAL_Delay(500);
	OLED_Clear();

	IMU_Initialise(&imu, &hi2c1, &huart3);
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	float pitch_accel, roll_accel, pitch_gyro, roll_gyro, millisNow, millisOld, pitch_CF, roll_CF, dt;
	pitch_gyro = roll_gyro = pitch_CF = roll_CF = 0;
	millisNow = millisOld = 0;
	float a = 0.95;
	int ledCount = 0;
	int oledCount = 0;

	while (1)
	{
		uint8_t sbuf[55] = "\n\r   ";

		// LED and buzzer thingamajic here, toggle every 100 iterations
		if (ledCount++ % 100 == 0)
		{
			ledCount = 1;
			HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_10);
			// HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_10);
		}

		// Pitch and roll from accelerometer
		IMU_AccelRead(&imu);
		IMU_GyroRead(&imu);
		pitch_accel = atan2(imu.acc[1], imu.acc[2]);
		roll_accel = atan2(imu.acc[0], imu.acc[2]);

		pitch_accel = pitch_accel / 3.14159265 * 180;
		roll_accel = roll_accel / 3.14159265 * 180;

		// Pitch and roll from gyroscope
		millisNow = HAL_GetTick();
		dt = millisNow - millisOld;
		millisOld = millisNow;
		pitch_gyro = pitch_gyro + imu.gyro[0] * dt * 0.001;
		roll_gyro = roll_gyro + imu.gyro[1] * dt * 0.001;

		// Complementary filter for pitch and roll
		pitch_CF = (1 - a) * pitch_accel + a * (pitch_CF + imu.gyro[0] * dt * 0.001);
		roll_CF = (1 - a) * roll_accel + a * (roll_CF + imu.gyro[1] * dt * 0.001);

		// Send data via UART
		sprintf(&sbuf[2], "%5.2f, %5.2f, %5.2f, %5.2f, %5.2f, %5.2f", pitch_accel, roll_accel, pitch_gyro, roll_gyro, pitch_CF, roll_CF);
		HAL_UART_Transmit(&huart3, sbuf, sizeof(sbuf), HAL_MAX_DELAY);

		if (oledCount++ % 25 == 0)
		{
			uint8_t oled_buf[20] = ":(";
			oledCount = 1;
			sprintf(oled_buf, "Pitch: %5.2f", pitch_CF);
			OLED_ShowString(0, 5, oled_buf);
			sprintf(oled_buf, "Roll : %5.2f", roll_CF);
			OLED_ShowString(0, 16, oled_buf);
			OLED_Refresh_Gram();
		}

		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
	/* USER CODE END 3 */
}

